int ft_memcmp(const void *s1, const void *s2, size_t n)
{
	const unsigned char *a = (unsigned char *)s1;
	const unsigned char *b = (unsigned char *)s2;
	size_t i;
	i = 0;
	
	while(i < n)
	{
		if(a[i] != b[i])
			return (a[i] - b[i]);
		i++;
	}
	return 0;
}
